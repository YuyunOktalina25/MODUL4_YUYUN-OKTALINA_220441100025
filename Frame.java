    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        // TODO add your handling code here:
         matkul = txmatkul.getText();
         deadline = txdeadline.getText();
         detail = txdetail.getText();
        //JIKA ADA SATU INPTAN YANG BELUM DIIISI//
         if (matkul.isEmpty() && deadline.isEmpty() && detail.isEmpty()||matkul.isEmpty()|| deadline.isEmpty()||detail.isEmpty()){
            JOptionPane.showMessageDialog(this,"Masukan Data Yang lengkap!");

         }else {
         //MENAMBAHKAN LIST YG SUDAD DIISI KEDALAM TAB DAFTAR TUGAS//
             
         list1.addElement(angka + "." + matkul + " (DL : " + deadline + ")");
         list2.addElement(detail);

          JOptionPane.showMessageDialog(this,"Data Telah diSimpan");
          angka++;
         }
    }//GEN-LAST:event_btsimpanActionPerformed

    private void ListTugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListTugasMouseClicked
        // TODO add your handling code here:
        //IBERFUNGS JIKA LIST TUGAS DIKLIK MAKA ANAN MUNCUL DETAIL TUGAS SESUAI INDEX//
         if (evt.getClickCount() == 1) {
            int pilihIndex = ListTugas.locationToIndex(evt.getPoint());
            if (pilihIndex >= 0) {
                // Mendapatkan nilai dari elemen yang dipilih
                String pilihdetail = list2.getElementAt(pilihIndex);
                txdetail2.setText(pilihdetail);
            }
        }
    }//GEN-LAST:event_ListTugasMouseClicked

    private void bthapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthapusActionPerformed
        // TODO add your handling code here:
        int index = ListTugas.getSelectedIndex();
        //untuk menghapus nomor list daftar tugas//
        if (index != -1) {
        list1.remove(index);
        list2.removeElementAt(index);
        
          for (int i = index ; i < list1.size(); i++) {
                String tugas = list1.getElementAt(i);
                int noUrutBaru = Integer.parseInt(tugas.substring(0, tugas.indexOf("."))) - 1;
                String newTugas = noUrutBaru + tugas.substring(tugas.indexOf("."));
                list1.set(i, newTugas);
            }
          } else {
            JOptionPane.showMessageDialog(this, "Pilih tugas yang ingin dihapus terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        txdetail2.setText(""); // Membersihkan area detail setelahÂ menghapus
          

        
       

    }//GEN-LAST:event_bthapusActionPerformed


